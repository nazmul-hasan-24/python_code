"""
Operator	Example	   Same As	
    =	    x = 5	    x = 5	
    +=	    x += 3	    x = x + 3	
    -=	    x -= 3	    x = x - 3	
    *=	    x *= 3	    x = x * 3	
    /=	    x /= 3	    x = x / 3	
    %=	    x %= 3	    x = x % 3	
    //=	    x //= 3	    x = x // 3	
    **=	    x **= 3	    x = x ** 3	
    &=	    x &= 3	    x = x & 3  Perform Bitwise AND	
    |=	    x |= 3	    x = x | 3  bitwise OR operation
    ^=	    x ^= 3	    x = x ^ 3   Bitwise XOR
    >>=	    x >>= 3	    x = x >> 3	 Right Shift
    <<=	    x <<= 3	    x = x << 3	Left shift
    :=	    print(x := 3)	x = 3   Assign value x = 3
    print(x)
"""